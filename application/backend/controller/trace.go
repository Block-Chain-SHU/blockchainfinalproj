// 人才培养追踪上链与查询接口

func Uplink(c *gin.Context) {
	// 与 userID 不同，追踪码取雪花ID从第二位开始的18位。
	// +--------------------------------------------------------------------------+
	// | 1 Bit Unused | 41 Bit Timestamp |  10 Bit NodeID  |   12 Bit Sequence ID |
	// +--------------------------------------------------------------------------+
	autoGeneratedTraceCode := pkg.GenerateID()[1:]
	args := buildArgs(c, autoGeneratedTraceCode)
	if args == nil {
		return
	}
	res, err := pkg.ChaincodeInvoke("Uplink", args)
	if err != nil {
		c.JSON(200, gin.H{
			"message": "uplink failed" + err.Error(),
		})
		return
	}
	c.JSON(200, gin.H{
		"code":              200,
		"message":           "uplink success",
		"txid":              res,
		"student_trace_id":  args[1],
	})
}

